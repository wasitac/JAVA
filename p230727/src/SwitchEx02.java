
public class SwitchEx02 {

	public static void main(String[] args) {
		
		String fruit = "사과";
		
		switch (fruit) {
		case "딸기": 
			System.out.println("딸기입니다.");
			break;
		case "사과":
			System.out.println("사과입니다.");
			break;
		case "배":
			System.out.println("배입니다.");
			break;
		}
	}

}
