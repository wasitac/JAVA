
public class TypeEx01 {

	public static void main(String[] args) {
		// 1. 변수선언
		int num;
		
		/*
		 * 2. 변수 초기화 
		 * 상수(constant) : 변하지 않는 값, 이름 있음 
		 * 리터럴(literal) : 변하지 않는 값, 이름 없음, 값 그 자체
		 * 정수형 리터럴 : 5		int
		 * 실수형 리터럴 : 2.5		double
		 * 문자형 리터럴 : 'a'		char
		 * 문자열 리터럴 : "abc"	String
		 * 논리형 리터럴 : true		boolean
		 */
		num = 0;
		
		// 3. 변수 num에 저장된 값 출력
		System.out.println(num);
		
		// 4. 변수 num에 저장된 값 변경
		num = 7;
		System.out.println(num);
	}

}
