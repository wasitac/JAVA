
public class OperatorEx05 {

	public static void main(String[] args) {
		// 증감 연산자
		int num = 5;
		System.out.println("num " + num);
		
		num += 1;
		System.out.println("num " + num);

		num++;
		System.out.println("num " + num);

		System.out.println("num " + num++);
		System.out.println("num " + num);
		System.out.println("num " + ++num);
		
	}

}
