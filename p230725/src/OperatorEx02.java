public class OperatorEx02 {

	public static void main(String[] args) {
		// 산술 연산자
		// 변수 선언과 동시에 초기화
		int one = 5, two = 3;
		System.out.println(one + two);
		System.out.println(one - two);
		System.out.println(one * two);
		System.out.println(one / two);
		System.out.println(one % two);
		
		System.out.println("덧셈 결과 " + (one + two));
		System.out.println("뺄셈 결과 " + (one - two));
		System.out.println("곱셈 결과 " + (one * two));
	}

}
